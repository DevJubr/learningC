#include <stdio.h>

int main () {
  printf("%d\n", 10*100);
  printf("%d\n", 50/100);
  printf("%d\n", 10-600);
  printf("%d\n", 10+30);
  return 0;
}

// - In the line ("%d\n") %d, the format specifier %d is used for calculation, and it represents the location where a calculated value will be inserted. The '\n' character is used to create a line break.